class LanguageService {
  static const String _languageKey = 'selected_language';
  static const String _defaultLanguage = 'English';

  static Future<String> getLanguage() async {
    try {
      final prefs = await SharedPreferences.getInstance();
      final language = prefs.getString(_languageKey) ?? _defaultLanguage;
      return language;
    } catch (e) {
      // Hapus data corrupt dan reset ke default
      try {
        final prefs = await SharedPreferences.getInstance();
        await prefs.remove(_languageKey);
      } catch (clearError) {
        // Silent error handling
      }
      return _defaultLanguage;
    }
  }

  static Future<void> setLanguage(String language) async {
    final prefs = await SharedPreferences.getInstance();
    await prefs.setString(_languageKey, language);
  }

  static String getLocalizedText(String key, String language) {
    switch (language) {
      case 'Indonesia':
        return _indonesianTexts[key] ?? key;
      case 'English':
      default:
        return _englishTexts[key] ?? key;
    }
  }

